- ログイン状態はブラウザに保持されます。ログアウトはハンバーガーメニューから

## イベントを追加する
1. 右上の「イベント追加」ボタン
2. 顧客選択モーダルで ANDPAD 顧客を検索（顧客名・カナ・住所・ANDPAD ID）
3. 検索ボタンを押す → 結果一覧から顧客をクリック
4. 案件名（必須）・イベント名・イベント日・設営/撤収日 などを入力して保存
5. 顧客を持たないイベントは「顧客選択なしでイベント追加」から登録可能

## 撮影依頼を追加する
1. 右上の「撮影依頼」ボタン
2. 顧客を検索 → 選択
3. 撮影可能期間・撮影種類（YouTube/スチール等）・各撮影日を入力
4. 立ち合い・施主在宅・注意事項などを記入して保存

## 既存行に顧客を後から紐づける
