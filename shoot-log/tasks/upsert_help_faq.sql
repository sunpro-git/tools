## 顧客情報
Q. 顧客名を直接入力したのに、ANDPAD連携の表示がされません
A. 顧客名フィールドは自由記述です。ANDPADと紐づけるには、顧客名右の「顧客選択」ボタンから ANDPAD 顧客を選んでください。顧客IDが設定され、ANDPADページへのリンクも表示されます。

Q. 既存のイベントにあとから顧客を紐づけられますか
A. はい。編集モーダルを開き、顧客名右の「顧客選択」ボタンをクリックしてください。住所・地理情報も同時に上書きされます。

Q. 「顧客なし」イベントを登録したい
A. 「イベント追加」→ 顧客選択モーダル右上の「顧客選択なしでイベント追加」ボタンから登録できます。

## イベント種別（eventType）
Q. ohirome / event の違いは何ですか
A. 自動判定の分類です:
- ohirome / ohirome_nashi: 名称に「お披露目会」「見学会」を含む（家具設営有/無）
- event: その他のイベント（お披露目会・見学会以外）
- satsuei / satsuei_nashi: 撮影日のみが登録された行（家具設営有/無）

Q. eventType を手動で変えたい
A. 通常は登録内容から自動判定されます。判定キーワード（例: お披露目会・見学会）を変更したい場合は、ハンバーガーメニュー > イベント種別設定 から正規表現を編集してください。

Q. 完成お披露目会と書いたのに event_type が ohirome になりません
A. イベント名フィールドではなく案件名フィールドに書いている場合も自動判定対象です。家具設営の有無で ohirome（あり）か ohirome_nashi（なし）に分かれます。

## カレンダー連携
Q. Google カレンダーに反映されません
A. 保存ボタンを押すと自動同期されます。DB を直接更新（外部スクリプト等）した場合は同期されません。編集モーダルで保存し直してください。

Q. 同じ予定が複数できてしまいました
A. イベント日（eventDates）を変更したとき、旧日付の予定が削除される前に画面を閉じた可能性があります。GAS の処理に5〜10秒かかることがあるため少し待ってから確認してください。残ってしまった場合は ハンバーガーメニュー > カレンダー予定一覧 から個別削除できます。

Q. 削除したのに Google カレンダーに残っています
A. 通常は削除時に自動でカレンダーからも消えます。残った場合は カレンダー予定一覧 から削除してください。

## 一覧表示
Q. 登録したのに一覧に表示されません
A. 上部の月範囲フィルター（〇月〜〇月）を確認してください。すべての日付フィールドが対象になっています。日付未設定の行は「日程未設定を表示」をオンにすると表示されます。

Q. 設営4月・イベント5月の予定が、5月にグループ表示されました
A. 仕様です。フィルター範囲内に最古日付がある場合、その月にグルーピングします。設営は4月だがフィルターが5月以降の場合、イベント5月で表示されます。

Q. 撮影タグが付かない行があります
A. 撮影タイプ（YouTube等）だけでは撮影バッジは出ません。撮影可能期間または各撮影日のいずれかが入力されると「撮影予定」等のバッジが表示されます。

## ステータスバッジ
Q. 「イベント」と「イベント予定」「イベント開催中」「イベント終了」の違いは
A. 開催日（eventDates）が登録されているかどうかで分かれます。
- 開催日なし → イベント（teal）
- 開催日があり、すべて未来 → イベント予定（青）
- 今日が開催期間内 → イベント開催中（緑）
- すべて過去 → イベント終了（グレー）

## 削除・編集
Q. 行を削除すると Chatwork 通知は飛びますか
A. 現状、削除時の通知は飛びません（保存時のみ通知）。

Q. 編集中に他の人が同じ行を編集していたらどうなりますか
A. 撮影依頼モーダル（saveProperty）では updatedAt を比較して、他者更新を検知すると保存失敗します。再度開き直して編集してください。

