## ステータスバッジの意味
**イベントステータス**
- イベント予定（青）：開催日が未来
- イベント開催中（緑）：今日が開催期間内
- イベント終了（グレー）：すべての開催日が過去
- イベント（teal）：開催日未設定

**撮影ステータス**
- 撮影予定/撮影中/撮影済（青/緑/グレー）：実際の撮影日が登録されている場合
- 撮影タイプのみで日付が未設定の行はバッジ表示なし

**イベント種別タグ**（紫）
- お披露目会・見学会（家具有/無）：名称に「お披露目会」「見学会」を含む場合
- イベント（その他）：イベント日が登録された汎用イベント
- 撮影のみ（家具有/無）：撮影日のみで構成される行

